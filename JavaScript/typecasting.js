// Type casting is the process of converting a value from one data type to another. In JavaScript, this often means converting between strings, numbers, and booleans. There are two types:

// Implicit type casting (type coercion): JavaScript automatically converts types when needed, e.g., "5" + 2 results in "52" (number 2 is converted to string).
// Explicit type casting: You manually convert types using functions like Number(), String(), or Boolean(), e.g., Number("5") results in 5.


let str = "123";
let num = Number(str); // Explicit type casting: string to number
console.log(num); // 123


